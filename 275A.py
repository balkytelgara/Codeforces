d = [list(map(int, input().split())) for _ in range(3)]
r = [[0, 0, 0], [0, 0, 0], [0, 0, 0]]

if d[0][0] % 2 == 1:
  r[0][0], r[0][1], r[1][0] = not r[0][0], \
                              not r[0][1], \
                              not r[1][0]

if d[0][1] % 2 == 1:
  r[0][0], r[0][1], r[0][2], r[1][1] = not r[0][0], \
                                       not r[0][1], \
                                       not r[0][2], \
                                       not r[1][1]
if d[0][2] % 2 == 1:
  r[0][1], r[0][2], r[1][2] = not r[0][1], \
                              not r[0][2], \
                              not r[1][2]
if d[1][0] % 2 == 1:
  r[0][0], r[1][0], r[1][1], r[2][0] = not r[0][0], \
                                       not r[1][0], \
                                       not r[1][1], \
                                       not r[2][0]
if d[1][1] % 2 == 1:
  r[1][0], r[1][1], r[1][2], r[0][1], r[2][1] = not r[1][0], \
                                                not r[1][1], \
                                                not r[1][2], \
                                                not r[0][1], \
                                                not r[2][1]
if d[1][2] % 2 == 1:
  r[1][2], r[0][2], r[2][2], r[1][1] = not r[1][2], \
                                       not r[0][2], \
                                       not r[2][2], \
                                       not r[1][1]
if d[2][0] % 2 == 1:
  r[2][0], r[1][0], r[2][1] = not r[2][0], \
                              not r[1][0], \
                              not r[2][1]
if d[2][1] % 2 == 1:
  r[2][1], r[2][0], r[2][2], r[1][1] = not r[2][1], \
                                       not r[2][0], \
                                       not r[2][2], \
                                       not r[1][1]
if d[2][2] % 2 == 1:
  r[2][2], r[2][1], r[1][2] = not r[2][2], \
                              not r[2][1], \
                              not r[1][2]

for i in r:
  n = ""
  for j in i:
    if j == 1:
      n += "0"
    else:
      n += "1"

  print(n)